export class Messages {

    channelId: string; 
    userId: string; 
    content: string; 
    timestamp: number; 

    constructor(channelId: string, userId: string, content: string, timestamp: number) {
        this.channelId = channelId;
        this.userId = userId;
        this.content = content;
        this.timestamp = timestamp;
    }
      
    





}